def parse_milisecond_timestamp(ts):
  return pd.to_datetime(ts, unit='ms')

df = pd.read_csv(
    'news.csv',
    sep='\t',
    header=None,
    index_col=0,
    names=["title", "url", "outlet", "category", "cluster", "host", "tstamp"],
    parse_dates=['tstamp'],
    date_parser=parse_milisecond_timestamp,
    dtype={
        "outlet": "category",
        "category": "category",
        "cluster": "category",
        "host": "category",
    },
)

df.groupby('outlet', sort=False)['title'].apply(
    lambda x: x.str.contains('Fed').sum()
).nlargest(10)

